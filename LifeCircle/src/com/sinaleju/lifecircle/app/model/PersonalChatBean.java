package com.sinaleju.lifecircle.app.model;

public class PersonalChatBean {

	private int id;
	private UserInfo user_info;
	private String content;
	private int have_read;
	private int unread_num;
	private int notice_unread_num;
	private int addtime;
	private int user_id;
	private int to_user_id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}


	public UserInfo getUser_info() {
		return user_info;
	}
	public void setUser_info(UserInfo user_info) {
		this.user_info = user_info;
	}


	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}


	public int getHave_read() {
		return have_read;
	}
	public void setHave_read(int have_read) {
		this.have_read = have_read;
	}


	public int getUnread_num() {
		return unread_num;
	}
	public void setUnread_num(int unread_num) {
		this.unread_num = unread_num;
	}


	public int getNotice_unread_num() {
		return notice_unread_num;
	}
	public void setNotice_unread_num(int notice_unread_num) {
		this.notice_unread_num = notice_unread_num;
	}


	public int getAddtime() {
		return addtime;
	}
	public void setAddtime(int addtime) {
		this.addtime = addtime;
	}


	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getTo_user_id() {
		return to_user_id;
	}
	public void setTo_user_id(int to_user_id) {
		this.to_user_id = to_user_id;
	}


	public static class UserInfo{
		private int id;
		private int type;
		private String display_name;
		private String header;
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		
		public int getType() {
			return type;
		}
		public void setType(int type) {
			this.type = type;
		}
		public String getDisplay_name() {
			return display_name;
		}
		public void setDisplay_name(String display_name) {
			this.display_name = display_name;
		}
		public String getHeader() {
			return header;
		}
		public void setHeader(String header) {
			this.header = header;
		}
	}
}
